class Stack:
    def __init__(self):
        self.stack =[]

    def push(self,item):
        self.stack.append(item)
        print(f"Pushed:{item}")

    def pop(self):
        if self.is_empty():
            return"Stack is empty"
        return(f"popped:{self.stack.pop()}")    

    def peek(self):
        if self.is_empty():
            return"Stack is empty"
        return(f"Top:{self.stack[-1]}")
    
    def is_empty(self):
        return len(self.stack)==0
    
    def size(self):
        return len(self.stack)
    
    def display(self):
        print("Stack (top -> bottom):",self.stack[::-1])

   
s = Stack() 
s.push(int(input("Enter")))

print(s.peek())
s.push(30)
s.display()
